async def demo_run():
    """
    演示运行 - 模拟真实场景
    """
    print("\n" + "=" * 70)
    print("AutoClaude 冲突解决系统 - 生产演示")
    print("=" * 70 + "\n")
    
    # 创建系统
    config = AutoClaudeConfig(
        max_parallel_agents=3,
        conflict_prediction_threshold=0.1,
        auto_resolve_conflicts=True
    )
    
    autoclaude = AutoClaude(config)
    
    # 注册Agent
    agents = [
        ("Auth-Agent", "authentication"),
        ("DB-Agent", "database"),
        ("API-Agent", "api"),
        ("Cache-Agent", "caching"),
        ("Log-Agent", "logging"),
    ]
    
    for agent_id, domain in agents:
        autoclaude.register_agent(agent_id, domain)
    
    # 创建任务 - 模拟真实项目场景
    tasks_config = [
        # 任务1-2: 会冲突（都修改auth.py）
        ("T1", "Auth-Agent", "实现JWT认证", ["auth.py", "models.py"], "authentication"),
        ("T2", "API-Agent", "添加OAuth集成", ["auth.py", "api/oauth.py"], "api"),
        
        # 任务3-4: 会冲突（都修改database.py）
        ("T3", "DB-Agent", "优化查询性能", ["database.py", "models.py"], "database"),
        ("T5", "Auth-Agent", "用户权限表更新", ["database.py"], "authentication"),
        
        # 任务5-6: 无冲突
        ("T4", "Cache-Agent", "实现Redis缓存", ["cache.py"], "caching"),
        ("T6", "Log-Agent", "添加日志中间件", ["middleware.py"], "logging"),
    ]
    
    for task_id, agent_id, desc, files, domain in tasks_config:
        autoclaude.create_task(task_id, agent_id, desc, files, domain)
    
    # 运行
    report = await autoclaude.run()
    
    # 显示结果
    print("\n" + "=" * 70)
    print("执行结果摘要")
    print("=" * 70)
    print(f"总任务数: {report['summary']['total_tasks']}")
    print(f"成功: {report['summary']['successful']}")
    print(f"失败: {report['summary']['failed']}")
    print(f"成功率: {report['summary']['success_rate']}")
    print(f"执行时间: {report['duration_seconds']}秒")
    print("=" * 70)
    
    return report

async def stress_test():
    """
    压力测试
    """
    print("\n" + "=" * 70)
    print("AutoClaude 压力测试")
    print("=" * 70 + "\n")
    
    config = AutoClaudeConfig(max_parallel_agents=5)
    autoclaude = AutoClaude(config)
    
    # 注册10个Agent
    for i in range(10):
        autoclaude.register_agent(f"Agent-{i}", f"domain-{i%5}")
    
    # 创建50个任务，故意制造冲突
    import random
    random.seed(42)
    
    for i in range(50):
        agent_id = f"Agent-{i%10}"
        domain = f"domain-{i%5}"
        
