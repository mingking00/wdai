class AutoClaude:
    """
    AutoClaude 主系统 - 生产版本
    """
    
    def __init__(self, config: Optional[AutoClaudeConfig] = None):
        self.config = config or AutoClaudeConfig()
        self.logger = Logger()
        self.coordinator = ConflictCoordinator()
        self.agents: Dict[str, ClaudeAgent] = {}
        self.tasks: List[Task] = []
        self.results: List[Dict] = []
        self.start_time = None
        
        self.logger.info("AutoClaude 系统初始化完成")
        self.logger.info(f"配置: {self.config.to_dict()}")
    
    def register_agent(self, agent_id: str, domain: str) -> ClaudeAgent:
        """注册Agent"""
        agent = ClaudeAgent(agent_id, domain, self.config, self.logger)
        self.agents[agent_id] = agent
        self.logger.info(f"注册Agent: {agent_id} (domain: {domain})")
        return agent
    
    def create_task(self, task_id: str, agent_id: str, description: str,
                   target_files: List[str], domain: str, 
                   dependencies: List[str] = None) -> Task:
        """创建任务"""
        task = Task(
            id=task_id,
            agent_id=agent_id,
            description=description,
            target_files=target_files,
            dependencies=dependencies or [],
            domain=domain
        )
        self.tasks.append(task)
        return task
    
    async def run(self) -> Dict:
        """
        运行AutoClaude系统
        """
        self.start_time = time.time()
        self.logger.info("=" * 60)
        self.logger.info("AutoClaude 系统启动")
        self.logger.info("=" * 60)
        
        # 1. 冲突预测与任务优化
        self.logger.info("【阶段1】冲突预测与任务优化")
        coordination = await self.coordinator.coordinate_task_assignment(self.tasks)
        
        self.logger.info(f"预测冲突数: {len(coordination['predicted_conflicts'])}")
        for conflict in coordination['predicted_conflicts']:
            self.logger.warning(
                f"  预测冲突: {conflict['task_a']} vs {conflict['task_b']} "
                f"(风险: {conflict['risk_score']:.2f}, 等级: {conflict['severity']})"
            )
        
        # 2. 按批次执行任务
        self.logger.info("【阶段2】任务执行")
        assignments = coordination['optimized_assignments']
        
        for i, assignment in enumerate(assignments):
            self.logger.info(f"  批次 {i+1}/{len(assignments)}: {assignment['type']}")
            
            batch_tasks = [t for t in self.tasks if t.id in assignment['tasks']]
            
            if assignment['type'] == 'sequential':
                # 串行执行
                for task in batch_tasks:
                    agent = self.agents.get(task.agent_id)
                    if agent:
                        result = await agent.execute_task(task, self.coordinator)
                        self.results.append(result)
            else:
                # 并行执行
                results = await asyncio.gather(*[
                    self.agents[t.agent_id].execute_task(t, self.coordinator)
                    for t in batch_tasks
                    if t.agent_id in self.agents
                ])
                self.results.extend(results)
        
        # 3. 生成报告
        elapsed = time.time() - self.start_time
        report = self._generate_report(elapsed)
        
        self.logger.info("=" * 60)
        self.logger.info("AutoClaude 执行完成")
        self.logger.info("=" * 60)
        
        return report
    
    def _generate_report(self, elapsed: float) -> Dict:
        """生成执行报告"""
        successful = sum(1 for r in self.results if r.get('success'))
        failed = len(self.results) - successful
        
        report = {
            'timestamp': datetime.now().isoformat(),
            'duration_seconds': round(elapsed, 2),
            'summary': {
                'total_tasks': len(self.tasks),
                'successful': successful,
                'failed': failed,
                'success_rate': f"{successful/len(self.results)*100:.1f}%" if self.results else "N/A"
            },
            'agents': {
                agent_id: {
                    'domain': agent.domain,
                    'status': agent.status,
                    'completed_tasks': len(agent.completed_tasks)
                }
                for agent_id, agent in self.agents.items()
            },
            'tasks': [
                {
                    'id': task.id,
                    'agent': task.agent_id,
                    'description': task.description,
                    'files': task.target_files,
                    'status': 'completed' if any(r['task'] == task.id and r['success'] for r in self.results) else 'failed'
                }
                for task in self.tasks
            ],
            'results': self.results
        }
        
        # 保存报告
        report_file = f".logs/autoclaude_report_{datetime.now().strftime('%Y%m%d_%H%M%S')}.json"
        with open(report_file, 'w') as f:
            json.dump(report, f, indent=2)
        
        self.logger.info(f"报告已保存: {report_file}")
        
        return report

